-- ============================================================
-- Phase 3 M7 Step 2.5 Rollback: admin_confirm_transfer 삭제
-- ============================================================
--
-- 배포 후 이상 감지 시 사용.
-- 주의: 이 롤백 스크립트 실행 전에 아래 조건을 먼저 확인할 것.
--   1) 코드가 이미 main에서 revert되어 더 이상 함수를 호출하지 않는 상태여야 함
--   2) 사용 중인 코드가 있는데 함수를 DROP하면 모든 입금 확인 API 호출이
--      "function does not exist" 에러로 실패한다.
--
-- 일반적으로는 이 롤백을 쓸 필요 없음.
-- 코드를 revert하면 함수는 잠재 상태로 남고, 기존 COUNT+UPDATE 경로가
-- 다시 사용되므로 함수를 강제 제거할 이유가 거의 없다.
-- 정말로 clean state가 필요할 때만 실행.
-- ============================================================

DROP FUNCTION IF EXISTS public.admin_confirm_transfer(UUID);
